##input函数的应用
a=input("提示：要输入的内容：")
b=input('提示：要输入的内容：')
print(int(a)+int(b))   #input输入函数的输入值为字符串类型，需要转换成整形后才能计算

    ##运算符号
print(1+1)    # 加法运算  2
print(1-1)    # 减法运算  0
print(2*4)    # 乘法运算  8
print(1/2)    # 除法运算  0.5
print(11//2)  # 5整除运算  5
print(11 % 2) # 取余运算  1
print(2**2)   # 2的2次方  4
  
print(9//4)     # 向上取余  2
print(-9//-4)   
print(9//-4)    # 一正一负的公式，向下取余  
print(-9//4)  
  
print(9 % -4)     # 公式：余数=被除数-除数*商  
print(-9 % 4)